package Operators;

public class Arithmetic {
public static void main(String[] args) {
    int a=8;
    int b=20;
       
    System.out.println(a+b);     //add operation
    System.out.println(b-a);     //subtract operation
    System.out.println(a*b);     // multiply operation
    System.out.println(b/a);     // divide operation gives quotiesnt ignores remainder
    System.out.println(b%a);     // modulus operator gives remainder
}
}
