package Operators;

public class Asssignment {
public static void main(String[] args) {
    int a=7;
    int b=9;

    a+=b;     //a=a+b
    System.out.println(a);

    int s=20;
    int d=15;
    s-=d;      //s=s-d
    System.out.println(s);



    int n=7;
    int m=9;
    n*=m;       //n=n*m
    System.out.println(n);


    int v=8;
    int c=9;
    c/=v;       //   c=c/v
    System.out.println(c);


    int x=36;
    int q=8;
    x%=q;          //x=x%q
    System.out.println(x);



}
}
